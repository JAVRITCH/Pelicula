    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
    try {
            int codigo = Integer.parseInt(lblIdCodigo.getText());
            String nombre = txtNombre.getText();
            int anoLanzamiento = Integer.parseInt(txtAnoLanzamiento.getText());
            String genero = txtGenero.getText();
            int duracion = Integer.parseInt(txtDuracion.getText());
            
            // Crear un objeto de tipo Pelicula con los datos ingresados
            Pelicula pelicula = new Pelicula(codigo,nombre, anoLanzamiento, genero, duracion);
            
            // Guardar la película en la base de datos
            pelicula.guardarDatosPelicula();
            JOptionPane.showMessageDialog(null, "Pelicula guardada correctamente.");
            
            // Limpiar los campos después de guardar
            lblIdCodigo.setText("");
            txtNombre.setText("");
            txtAnoLanzamiento.setText("");
            txtGenero.setText("");
            txtDuracion.setText("");
            
            // Actualizar la tabla de películas
            ConsultarPeliculas();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Error: Los campos de año de lanzamiento y duración deben contener valores numéricos válidos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void tablaPeliculasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPeliculasMouseClicked
        int fila = tablaPeliculas.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tablaPeliculas.getModel();

        lblIdCodigo.setText(model.getValueAt(fila, 0).toString());
        txtNombre.setText(model.getValueAt(fila, 1).toString());
        txtAnoLanzamiento.setText(model.getValueAt(fila, 2).toString());
        txtGenero.setText(model.getValueAt(fila, 3).toString());
        txtDuracion.setText(model.getValueAt(fila, 4).toString());

    }//GEN-LAST:event_tablaPeliculasMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    int codigo = Integer.parseInt(lblIdCodigo.getText());
    
    Pelicula pelicula = new Pelicula();
    pelicula.setId(codigo); // Establecer solo el código
    
    pelicula.eliminarDatosPelicula(codigo);
    JOptionPane.showMessageDialog(null, "Pelicula eliminada correctamente.");
    lblIdCodigo.setText("");
    txtNombre.setText("");
    txtAnoLanzamiento.setText("");
    txtGenero.setText("");
    txtDuracion.setText("");

    // Consultar los datos nuevamente y actualizar la tabla
    ConsultarPeliculas();
    }//GEN-LAST:event_btnEliminarActionPerformed

