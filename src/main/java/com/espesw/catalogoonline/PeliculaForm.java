public void LimpiarTabla() {
        DefaultTableModel model = (DefaultTableModel) tablaPeliculas.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }

    public void ConsultarPeliculas() {
        LimpiarTabla();
        DefaultTableModel model = (DefaultTableModel) tablaPeliculas.getModel();
        try {
            Pelicula pelicula = new Pelicula();
            ArrayList<Pelicula> peliculas = pelicula.obtenerPeliculas();
            for (Pelicula p : peliculas) {
                model.addRow(new Object[]{p.getId(), p.getNombre(), p.getAnoLanzamiento(), p.getGenero(), p.getDuracion()});
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al consultar películas: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
 }




